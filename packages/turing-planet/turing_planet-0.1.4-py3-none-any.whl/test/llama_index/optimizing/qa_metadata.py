# Q:火灾分为哪几种情形以及对应的处理单位
# A:火灾分为以下几种情形以及对应的处理单位：
#
# 1. 灾害规模大、情况危险复杂、灭火救援难度大的火灾事故，或可能达上一般级别以上的火灾事故。对于这种情况，市指挥部办公室会迅速通知医疗救护、生态环境、市场监督管理、供水、供电、供气等有关单位以及技术专家到场，配合消防救援机构的作战行动。
#
# 2. 对于先期处置和部门联动处置未能控制事态发展、消除危害的较大级别以上火灾事故，由市指挥部办公室报市政府分管副市长批准，在现场指挥部基础上成立市指挥部，市指挥部总指挥、副总指挥立即赶赴现场。事发地县区、市政府新闻办、市应急管理局、市城乡建设局、市生态环境局、市市场监督管理局、市民政局等单位负责人，应当立即启动本单位应急响应，并在第一时间赶到现场，接受市指挥部的统一指挥。
#
# 3. 当灾情继续扩大达到重大、特大级别时，立即报市长决定，报请省政府、国务院提高应急响应级别，在省政府、国务院及其相关应急指挥机构指导下开展工作。<end>
